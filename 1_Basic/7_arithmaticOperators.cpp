#include <iostream>

int main(){

//  arithmatic operators 

  int students = 25;

//  +  Addition
  students = students + 5;
  students +=5;

//  -  subtraction  
  students = students - 5;
  students -=5;

//  *  Multiplication
  students = students * 5;
  students *=5;

//  /  Division
  students = students / 5;
  students /=5;

//  %  Modulus (remainder from division)
  students = students % 5;
  students %=5;

  //  ++ Increment
  students = students++;

  //  -- Decrement
  students = students--;

return 0;
}